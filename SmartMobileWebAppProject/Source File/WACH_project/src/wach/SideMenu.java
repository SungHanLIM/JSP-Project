package wach;

public class SideMenu {
	
	/* SideMenu Section */ 
	// Side Menu 멤버변수 선언 :: 사이드 메뉴 정보
	
	private int s_id;
	private String s_name;
	private String s_picture;
	
	public int getSideMenu_id() {
		return s_id;
	}
	public void setSideMenu_id(int s_id) {
		this.s_id = s_id;
	}
	public String getSideMenu_name() {
		return s_name;
	}
	public void setSideMenu_name(String s_name) {
		this.s_name = s_name;
	}
	public String getSideMenu_picture() {
		return s_picture;
	}
	public void setSideMenu_picture(String s_picture) {
		this.s_picture = s_picture;
	}
	 /* End of the Side Menu Section */
}

